package com.futbolito.to;

public class RecintoTO {
	
	private int idRecinto;
	private String nombre;
	private String direccion;
	private String telefono;
	private int numeroCanchas;
	
	
	
	public int getIdRecinto() {
		return idRecinto;
	}
	public void setIdRecinto(int idRecinto) {
		this.idRecinto = idRecinto;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public int getNumeroCanchas() {
		return numeroCanchas;
	}
	public void setNumeroCanchas(int numeroCanchas) {
		this.numeroCanchas = numeroCanchas;
	}
	
	
	

}
